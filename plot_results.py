"""
This script is responsible for visualizing the performance data generated by
`data_gen.py`. It reads the consolidated `performance_log.txt` file, parses it,
and generates a log-log plot of the clustering algorithm's runtime versus the
number of data points (nodes).

The primary output is a PNG image (`figure1_runtime_scaling.png`) that shows
the scaling behavior of the algorithm. This is crucial for analyzing its
efficiency and suitability for large datasets.

Author: Himanshu S. Mazumdar
Date: 2026-03-03
Version: 1.0

Key components:
- Configuration: Defines the path to the input log file and the output directory.
- `parse_log_file`: A function to read the key-value formatted log file and
  convert it into a structured pandas DataFrame.
- `generate_scaling_plot`: A function that takes the DataFrame and uses
  matplotlib to create and save the log-log scaling plot.
- `main`: The main function that orchestrates the process of reading the log,
  parsing it, and generating the plot.
"""
import pandas as pd
import matplotlib.pyplot as plt
import os

# --- Configuration ---
# This section defines the file paths for input and output.
# Reviewers should ensure these paths are consistent with the output of `data_gen.py`.

# Path to the performance log file. This file is the input for the plotting script.
LOG_FILE = os.path.join(os.path.dirname(__file__), 'results', 'performance_log.txt')

# Directory where the generated plot image will be saved.
OUTPUT_DIR = os.path.join(os.path.dirname(__file__), 'results')


def parse_log_file(log_path, log_callback):
    """
    Parses the performance log file, which is in a key-value pair format.

    The log file is not a standard CSV. Each line represents a single experiment
    run, with metrics like 'Nodes', 'Clustering_s', etc. This function reads
    each line, splits it into key-value pairs, and constructs a pandas DataFrame.

    Args:
        log_path (str): The absolute path to the `performance_log.txt` file.
        log_callback (function): A function to call for logging output.

    Returns:
        pandas.DataFrame: A DataFrame containing the parsed performance data,
                          with columns for each metric.
    """
    log_callback(f"Attempting to parse log file: {log_path}")
    records = []
    try:
        with open(log_path, 'r') as f:
            for line in f:
                record = {}
                parts = line.strip().split(', ')
                for part in parts:
                    if ': ' in part:
                        key, value = part.split(': ', 1)
                        record[key] = value
                if record:
                    records.append(record)
        
        df = pd.DataFrame(records)
        
        # --- Data Cleaning and Preparation ---
        # The raw data is in string format and needs to be converted to numeric
        # types for plotting. Any rows with conversion errors are dropped.

        required_cols = ['Nodes', 'DM_Build_s', 'Clustering_s']
        if not all(col in df.columns for col in required_cols):
            log_callback(f"Error: Log file is missing one of the required columns: {required_cols}")
            return pd.DataFrame()

        for col in required_cols:
            df[col] = pd.to_numeric(df[col], errors='coerce')

        df.dropna(subset=required_cols, inplace=True)

        # The 'DM_Build_s' metric was found to be invalid (-1.00).
        # We therefore use only the 'Clustering_s' as the total time for the plot.
        df['Total_Time_s'] = df['Clustering_s']
        
        log_callback("Log file parsed successfully.")
        return df
    except Exception as e:
        log_callback(f"Error parsing log file: {e}")
        return pd.DataFrame() # Return an empty DataFrame on error.

def generate_scaling_plot(df, log_callback):
    """
    Generates and saves a log-log plot of runtime vs. number of nodes.

    This function takes the cleaned DataFrame, groups the data to handle any
    duplicate runs, and then uses matplotlib to create a log-log plot.
    This type of plot is standard for analyzing algorithmic complexity.

    Args:
        df (pandas.DataFrame): The DataFrame containing the performance data.
        log_callback (function): A function to call for logging output.
    """
    log_callback("Attempting to generate plot...")
    if df.empty:
        log_callback("DataFrame is empty. Cannot generate plot.")
        return None

    # In case of multiple runs for the same number of nodes, we take the best
    # (minimum) time to be representative of the algorithm's capability.
    df_agg = df.groupby('Nodes')['Total_Time_s'].min().reset_index()
    df_agg = df_agg.sort_values('Nodes')

    log_callback("--- Data for Plot ---")
    log_callback(str(df_agg))

    try:
        plt.figure(figsize=(10, 6))
        plt.plot(df_agg['Nodes'], df_agg['Total_Time_s'], marker='o', linestyle='-')
        
        plt.title('HSM-Cluster: Empirical Runtime vs. Dataset Size')
        plt.xlabel('Number of Points (N)')
        plt.ylabel('Total Runtime (seconds)')
        plt.grid(True, which="both", ls="--")
        
        # Use a log-log scale to visualize the scaling behavior.
        # A straight line on a log-log plot indicates polynomial complexity.
        plt.xscale('log')
        plt.yscale('log')
        
        # Annotate each data point with its runtime value for clarity.
        for i, row in df_agg.iterrows():
            plt.text(row['Nodes'], row['Total_Time_s'], f'{row["Total_Time_s"]:.2f}s', fontsize=9, ha='right')

        plot_path = os.path.join(OUTPUT_DIR, 'figure1_runtime_scaling.png')
        
        if not os.path.exists(OUTPUT_DIR):
            os.makedirs(OUTPUT_DIR)
            
        plt.savefig(plot_path)
        log_callback(f"\nPlot saved to: {plot_path}")

        # Verify that the plot file was actually created.
        if os.path.exists(plot_path):
            log_callback("Verification successful: Plot file exists.")
        else:
            log_callback("Verification FAILED: Plot file does not exist.")
        
        return plot_path
            
    except Exception as e:
        log_callback(f"Error during plot generation: {e}")
        return None

def run_plotting(log_callback):
    """
    Main function to orchestrate the parsing and plotting process.
    """
    log_callback("Script starting...")
    plot_path = None
    try:
        if not os.path.exists(LOG_FILE):
            log_callback(f"Error: Log file not found at {LOG_FILE}")
            return None

        # 1. Parse the log file into a DataFrame.
        df = parse_log_file(LOG_FILE, log_callback)
        
        # 2. Generate and save the plot from the DataFrame.
        if df is not None and not df.empty:
            plot_path = generate_scaling_plot(df, log_callback)
        else:
            log_callback("Could not generate plot due to parsing error or empty log file.")
            
    except Exception as e:
        log_callback(f"An unexpected error occurred in main: {e}")
    finally:
        log_callback("Script finished.")
    
    return plot_path

def main():
    """
    Main function to orchestrate the parsing and plotting process.
    """
    run_plotting(print)

if __name__ == "__main__":
    # This ensures the main function is called only when the script is executed directly.
    main()
